FRED_BASE = "https://api.stlouisfed.org/fred/series/observations"

def fred_fetch(series_id: str,
               output_type: int = 1,
               obs_start: str | None = None,
               obs_end: str | None = None,   # None → uses current END_DATE at call time
               realtime_start: str = "1900-01-01",
               realtime_end: str = "9999-12-31") -> pd.Series:
    """
    Fetch from FRED API. obs_start=None omits the parameter → returns
    all data from series inception (FRED default behaviour).
    In replicate mode, output_type=1 calls are pinned to the last available
    vintage on or before REPLICATE_VINTAGE_CUTOFF (via get_last_vintage_before).
    """
    effective_end = obs_end if obs_end is not None else END_DATE
    params = dict(series_id=series_id,
                  observation_end=effective_end,
                  output_type=output_type,
                  api_key=FRED_API_KEY,
                  file_type="json")
    if obs_start is not None:
        params["observation_start"] = obs_start
    # Vintage queries always need realtime params
    if output_type != 1:
        params["realtime_start"] = realtime_start
        params["realtime_end"]   = realtime_end
    # In replicate mode, pin current-vintage calls to series-specific vintage date
    elif MODE == "replicate":
        vdate = get_last_vintage_before(series_id)
        params["realtime_start"] = vdate
        params["realtime_end"]   = vdate

    for attempt in range(3):
        try:
            r = requests.get(FRED_BASE, params=params, timeout=30)
            r.raise_for_status()
            payload = r.json()
            if "observations" not in payload:
                raise ValueError(payload.get("error_message", "no observations"))
            obs = pd.DataFrame(payload["observations"])
            obs["date"]  = pd.to_datetime(obs["date"])
            obs["value"] = pd.to_numeric(obs["value"], errors="coerce")
            return obs.set_index("date")["value"].dropna().sort_index()
        except Exception as e:
            if attempt == 2:
                print(f"    ⚠ {series_id}: {e}")
            time.sleep(2 ** attempt)
    return pd.Series(dtype=float)


def get_last_vintage_before(series_id: str) -> str:
    """
    Returns the last available vintage date for `series_id` on or before
    REPLICATE_VINTAGE_CUTOFF, by querying the FRED vintage_dates API.
    Only called in MODE == "replicate".
    Falls back to per-series hardcoded dates (verified from ALFRED) if the
    API call fails.
    """
    try:
        r = requests.get(
            "https://api.stlouisfed.org/fred/series/vintagedates",
            params={"series_id": series_id,
                    "realtime_end": REPLICATE_VINTAGE_CUTOFF,
                    "api_key": FRED_API_KEY,
                    "file_type": "json"},
            timeout=10)
        r.raise_for_status()
        dates = r.json().get("vintage_dates", [])
        if dates:
            return dates[-1]   # last = most recent on or before cutoff
    except Exception:
        pass   # fall through to hardcoded fallback
    # Hardcoded fallback (verified manually from ALFRED)
    KNOWN_VINTAGES = {
        "INDPRO"  : "2020-03-17",
        "W875RX1" : "2020-03-27",
        "ICSA"    : "2020-03-26",
        "PAYEMS"  : "2020-03-06",
        "PCE"     : "2020-02-28",
        "GDPC1"   : "2020-02-27",
        "PRFI"    : "2020-02-27",
        "PNFI"    : "2020-02-27",
    }
    fallback = KNOWN_VINTAGES.get(series_id, REPLICATE_VINTAGE_CUTOFF)
    print(f"    vintage_dates fallback: {series_id} → {fallback}")
    return fallback


def to_me(s: pd.Series) -> pd.Series:
    """Snap to month-end, deduplicate."""
    if len(s) == 0 or not isinstance(s.index, pd.DatetimeIndex):
        return pd.Series(dtype=float)
    s = s.copy()
    s.index = s.index + pd.offsets.MonthEnd(0)
    return s.groupby(level=0).last()


def to_qe(s: pd.Series) -> pd.Series:
    """Snap to quarter-end, deduplicate."""
    if len(s) == 0:
        return pd.Series(dtype=float)
    s = s.copy()
    s.index = pd.DatetimeIndex(s.index) + pd.offsets.QuarterEnd(0)
    return s.groupby(level=0).last()


def zscore(s: pd.Series) -> pd.Series:
    mu, sig = s.dropna().mean(), s.dropna().std()
    return (s - mu) / sig if sig > 0 else s * np.nan


def make_monthly_index(end: str = END_DATE) -> pd.DatetimeIndex:
    return pd.date_range(start=START_DATE, end=end, freq="ME")


def build_rt_mom(series_id: str, name: str,
                 obs_start: str = "1939-01-01") -> pd.Series:
    """
    First-release MoM% for absolute-unit series (safe for ALFRED output_type=4).
    Stitches: first-release where available, current vintage otherwise.
    Seam month uses current-vintage MoM.
    """
    # output_type=4 needs explicit realtime window
    lev_fr_raw = fred_fetch(series_id, output_type=4, obs_start=obs_start,
                            realtime_start="1900-01-01", realtime_end="9999-12-31")
    lev_fr = to_me(lev_fr_raw)
    lev_cv = to_me(fred_fetch(series_id, output_type=1, obs_start=obs_start))

    mom_fr = np.log(lev_fr.replace(0, np.nan)).diff(1)
    mom_cv = np.log(lev_cv.replace(0, np.nan)).diff(1)

    combined = mom_fr.combine_first(mom_cv)
    source   = pd.Series("cv", index=combined.index)
    rt_mask  = mom_fr.notna().reindex(combined.index, fill_value=False)
    source[rt_mask] = "rt"
    for d in combined.index[source != source.shift(1)]:
        if d in mom_cv.index and not pd.isna(mom_cv.get(d, np.nan)):
            combined[d] = mom_cv[d]

    print(f"  {name:20s}: RT={rt_mask.sum()}  CV={(~rt_mask).sum()}")
    return combined.rename(name)


def build_rt_qoq(series_id: str, name: str) -> pd.Series:
    """
    First-release QoQ log-diff for quarterly series.
    """
    lev_fr = to_qe(fred_fetch(series_id, output_type=4, obs_start="1947-01-01"))
    lev_cv = to_qe(fred_fetch(series_id, output_type=1, obs_start="1947-01-01"))

    # QoQ log-diff separately on each leg
    logdiff_fr = np.log(lev_fr.replace(0, np.nan)).diff(1)
    logdiff_cv = np.log(lev_cv.replace(0, np.nan)).diff(1)

    combined = logdiff_fr.combine_first(logdiff_cv)
    n_rt = logdiff_fr.notna().sum()
    print(f"  {name:20s}: RT={n_rt}  CV={combined.notna().sum()-n_rt}")
    return combined.rename(name)


