def compare_with_author(mat_file: str,
                        yy_monthly_z: pd.DataFrame,
                        yy_quarterly_z: pd.DataFrame,
                        monthly_idx: pd.DatetimeIndex) -> None:
    """
    Load the author's original .mat file and print a correlation table
    between author series (series_01..15) and our replicated series.

    The .mat file contains double arrays named 'unknown_01' .. 'unknown_15'
    (or similar). Series 01-12 are monthly, 13-15 are quarterly (non-NaN
    only at quarter-end months).
    """
    try:
        import scipy.io as sio
    except ImportError:
        print("  ⚠ scipy not installed — pip install scipy")
        return

    try:
        mat = sio.loadmat(mat_file)
    except Exception as e:
        print(f"  ⚠ Could not load {mat_file}: {e}")
        return

    # .mat stores y_monthly (T×12) and y_quarterly (T×3) separately
    print(f"  .mat keys: {[k for k in mat if not k.startswith('_')]}")
    ym = mat["y_monthly"]
    yq = mat["y_quarterly"]
    if ym.shape[0] < ym.shape[1]: ym = ym.T
    if yq.shape[0] < yq.shape[1]: yq = yq.T
    author_idx = pd.date_range("1950-01-01", "2019-12-31", freq="ME")
    n_m = min(ym.shape[0], len(author_idx))
    n_q = min(yq.shape[0], len(author_idx))
    author_m = pd.DataFrame(ym[:n_m,:], index=author_idx[:n_m],
                             columns=[f"author_{i+1:02d}" for i in range(ym.shape[1])])
    author_q = pd.DataFrame(yq[:n_q,:], index=author_idx[:n_q],
                             columns=[f"author_{i+1:02d}" for i in range(12, 12+yq.shape[1])])
    author_df = pd.concat([author_m, author_q], axis=1)
    print(f"  Author df: {author_df.shape}")

    # Replicated series restricted to 1950-2019 (author's sample)
    # Z-score locally for correlation comparison only
    rep_m_z = yy_monthly_z.loc["1950-01-01":"2019-12-31"].apply(zscore)
    rep_q_z = yy_quarterly_z.loc["1950-01-01":"2019-12-31"].apply(zscore)
    rep_all = pd.concat([rep_m_z, rep_q_z], axis=1)
    our_cols = list(rep_m_z.columns) + list(rep_q_z.columns)

    from scipy import stats as _stats
    print(f"\n{'='*75}")
    print("  CORRELATION: replicated vs author (1950-2019)")
    print("  (*** p<0.01  ** p<0.05  * p<0.10)")
    print(f"{'='*75}")
    print(f"  {'#':>3s}  {'Replicated series':45s}  {'corr':>8s}  {'n':>5s}  sig")
    print(f"  {'-'*73}")

    for i in range(min(len(our_cols), 15)):
        our_col  = our_cols[i]
        auth_col = f"author_{i+1:02d}"
        if auth_col not in author_df.columns or our_col not in rep_all.columns:
            print(f"  {i+1:>3d}  {'n/a':45s}  {'n/a':>8s}")
            continue
        auth_s   = author_df[auth_col].replace(0, np.nan)
        combined = pd.DataFrame({"a": auth_s, "r": rep_all[our_col]}).dropna()
        n = len(combined)
        if n > 20:
            corr, pval = _stats.pearsonr(combined["a"], combined["r"])
            stars = "***" if pval < 0.01 else "**" if pval < 0.05 else "*" if pval < 0.10 else ""
        else:
            corr, pval, stars = np.nan, np.nan, ""
        print(f"  {i+1:>3d}  {our_col:45s}  {corr:+8.4f}  {n:>5d}  {stars}")



def build_dataset(
    fred_api_key:             str | None = None,
    data_dir:                 Path | str | None = None,
    mode:                     str | None = None,
    replicate_vintage_cutoff: str | None = None,
    author_mat_file:          str | None = None,
) -> dict:
    """
    Build the Gomez-Cram JF 2022 macro-financial dataset.

    All arguments default to the module-level constants so the script can
    be run standalone. When called from main_BC.py, arguments are passed
    explicitly — the user only needs to configure main_BC.py.

    Parameters
    ----------
    fred_api_key             : FRED API key (free at fred.stlouisfed.org)
    data_dir                 : folder containing hMvMd.xlsx; pkl saved here
    mode                     : "replicate" or "latest"
    replicate_vintage_cutoff : vintage cutoff date for replicate mode
    author_mat_file          : optional .mat path for correlation table

    Naming conventions used in output columns
    ------------------------------------------
    SA          : Seasonally Adjusted
    LogMoM      : Log month-on-month difference  log(x_t / x_{t-1})
    LogYoY      : Log year-on-year difference     log(x_t / x_{t-12})
    LogQoQ      : Log quarter-on-quarter diff     log(x_t / x_{t-1})  (quarterly)
    Log11mNeg   : Negated 11-month log difference −log(x_t / x_{t-11})
    RT          : Real-time — first-release vintage via ALFRED (output_type=4)
    LastVint    : Last available vintage as of REPLICATE_VINTAGE_CUTOFF
                  (replicate mode) or today (latest mode)
    PhillyRT    : First-release from Philly Fed Real-Time Data Set (hMvMd.xlsx)
    _neg        : Series entered negated so that higher = better conditions
                  (e.g. VIX_neg, BAA_AAA_neg, TED_neg)

    All series are z-scored over their full available sample before return.

    Parameters
    ----------
    author_mat_file : str, optional
        Path to the author's original .mat file
        (dataMacroFinance_1950_2019_updated.mat). If provided, prints a
        correlation table between author series (series_01..15) and the
        replicated series.
    """
    # Resolve arguments — passed values override module-level defaults
    global FRED_API_KEY, DATA_DIR, MODE, REPLICATE_VINTAGE_CUTOFF, END_DATE, PHILLY_H_FILE
    if fred_api_key             is not None: FRED_API_KEY             = fred_api_key
    if data_dir                 is not None: DATA_DIR                 = Path(data_dir)
    if mode                     is not None: MODE                     = mode
    if replicate_vintage_cutoff is not None: REPLICATE_VINTAGE_CUTOFF = replicate_vintage_cutoff

    if MODE == "replicate":
        END_DATE = "2019-12-31"
    elif MODE == "latest":
        import datetime
        END_DATE = datetime.date.today().strftime("%Y-%m-%d")
    else:
        raise ValueError(f"MODE must be 'replicate' or 'latest', got '{MODE}'")

    PHILLY_H_FILE = DATA_DIR / "hMvMd.xlsx"

    print("=" * 65)
    print("  Building Gomez-Cram JF 2022 Dataset")
    print("=" * 65)

    fred = Fred(api_key=FRED_API_KEY)
    monthly_idx = make_monthly_index(end=END_DATE)

    # Build raw series
    yy_monthly_macro = get_monthly_macro(fred, PHILLY_H_FILE)
    yy_monthly_fin   = get_monthly_financial()
    yy_quarterly     = get_quarterly_macro()
    nber_rec, qtr_end = get_indicators(fred, monthly_idx)

    # ── Merge macro + financial into single yy_monthly (cols 01-12) ──────────
    yy_monthly = pd.concat([yy_monthly_macro, yy_monthly_fin], axis=1)

    # ── In latest mode: trim to last date where ALL series have data ──────────
    if MODE == "latest":
        # Core macro series that must all have data (exclude short-history
        # financial series like T10YIE/VIX/TED which have known start dates
        # but are not the binding constraint on the end date)
        core_monthly_cols = [
            "IndProd_SA_LogMoM_LastVint",
            "NonFarmPayrolls_SA_LogMoM_RT",
            "PCE_SA_LogMoM_RT",
            "RealPrivInc_ExTransf_SA_LogMoM_LastVint",
            "AggWeeklyHours_SA_LogMoM_PhillyRT",
            "InitialClaims_SA_Log11mNeg_LastVint",
            "SP500_LogYoY",
            "BAA_AAA_neg",
        ]
        last_m = yy_monthly[
            [c for c in core_monthly_cols if c in yy_monthly.columns]
        ].apply(lambda s: s.last_valid_index()).min()
        last_q = yy_quarterly.apply(lambda s: s.dropna().index.max()).min()
        eff_end_raw = min(last_m, last_q)
        # Snap back to nearest quarter-end
        months_back = eff_end_raw.month % 3
        eff_end = (eff_end_raw - pd.DateOffset(months=months_back)
                   + pd.offsets.MonthEnd(0))
        yy_monthly   = yy_monthly.loc[:eff_end]
        yy_quarterly = yy_quarterly.loc[:eff_end]
        nber_rec     = nber_rec.loc[:eff_end]
        qtr_end      = qtr_end.loc[:eff_end]
        monthly_idx  = yy_monthly.index
        print(f"\n  Latest mode: effective end = {eff_end.date()}  "
              f"(binding: last_monthly={last_m.date()}  last_qtr={last_q.date()})")
    
    # Z-scoring is handled in main_BC.py (recursive or full-sample depending
    # on ZSCORE_METHOD config). Raw series are saved here — do not z-score.
    yy_monthly_z   = yy_monthly    # alias for compare_with_author only
    yy_quarterly_z = yy_quarterly  # alias for compare_with_author only

    # Summary stats on raw series
    print("\n── Monthly raw series stats (macro + financial, cols 01-12) ──")
    for col in yy_monthly.columns:
        s = yy_monthly[col].dropna()
        first = s.first_valid_index().strftime("%Y-%m") if len(s) > 0 else "n/a"
        print(f"  {col:22s}: n={len(s):4d}  first={first}  "
              f"[{s.min():.4f}, {s.max():.4f}]")

    print("\n── Quarterly raw series stats ───────────────────────────────")
    for col in yy_quarterly.columns:
        qe_only = yy_quarterly.loc[qtr_end == 1, col].dropna()
        print(f"  {col:22s}: n={len(qe_only):4d} quarter-end obs")

    print("\n── Quarterly z-score stats ──────────────────────────────────")
    for col in yy_quarterly_z.columns:
        qe_only = yy_quarterly_z.loc[qtr_end == 1, col].dropna()
        print(f"  {col:22s}: n={len(qe_only):4d} quarter-end obs")



    # ── Optional: compare with author's original series ─────────────────────
    if author_mat_file is not None:
        compare_with_author(
            author_mat_file, yy_monthly_z, yy_quarterly_z, monthly_idx)

    # ── Save pkl for use by main_BC.py ───────────────────────────────────────
    import pickle
    _out = dict(
        yy_monthly       = yy_monthly,       # raw — z-scoring done in main_BC.py
        yy_quarterly     = yy_quarterly,     # raw — z-scoring done in main_BC.py
        yy_monthly_raw   = yy_monthly,       # alias kept for backwards compatibility
        yy_quarterly_raw = yy_quarterly,     # alias kept for backwards compatibility
        nber_rec         = nber_rec,
        qtr_end          = qtr_end,
        monthly_idx      = monthly_idx,
        T                = len(monthly_idx),
        N_m              = yy_monthly.shape[1],
        N_q              = yy_quarterly.shape[1],
        MODE             = MODE,
    )
    _pkl_path = DATA_DIR / f"GC_jf2022_dataset_{MODE}.pkl"
    with open(_pkl_path, "wb") as f:
        pickle.dump(_out, f)
    print(f"\nSaved → {_pkl_path}")

    return _out


