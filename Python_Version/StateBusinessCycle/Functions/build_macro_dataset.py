def get_indicators(fred: Fred, monthly_idx: pd.DatetimeIndex) -> tuple:
    """Returns (nber_rec, qtr_end) both aligned to monthly_idx."""
    print("\n── Indicators ───────────────────────────────────────────────")

    # NBER recession
    rec = fred_fetch("USREC", obs_start=START_DATE)
    rec = to_me(rec).astype(int)
    nber_rec = rec.reindex(monthly_idx).rename("nber_recession")

    # Quarter-end flag
    qtr_end = pd.Series(
        [1 if m in (3,6,9,12) else 0 for m in monthly_idx.month],
        index=monthly_idx, name="qtr_end", dtype=int)

    print(f"  NBER recession: {nber_rec.sum()} recession months")
    return nber_rec, qtr_end


