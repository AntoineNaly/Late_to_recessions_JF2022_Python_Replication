def get_monthly_macro(fred: Fred, philly_h_file: Path) -> pd.DataFrame:
    """
    Returns DataFrame with columns:
      ip, employ, pce, real_pi, hours, initial_claims
    All are MoM% (decimal or percentage, consistent with z-scoring).
    """
    print("\n── Monthly macro series ─────────────────────────────────────")
    idx = make_monthly_index(end=END_DATE)
    cols = {}

    # 01 — Industrial Production (INDPRO): current vintage MoM%
    print("  Fetching INDPRO (current vintage) ...")
    indpro_d = fred_fetch("INDPRO", obs_start="1919-01-01")
    indpro   = indpro_d.resample("ME").last() if len(indpro_d) > 0 else pd.Series(dtype=float)
    cols["IndProd_SA_LogMoM_LastVint"] = np.log(indpro.replace(0, np.nan)).diff(1).reindex(idx)

    # 02 — Nonfarm Payrolls (PAYEMS): ALFRED first-release MoM%
    print("  Fetching PAYEMS (first-release RT) ...")
    cols["NonFarmPayrolls_SA_LogMoM_RT"] = build_rt_mom("PAYEMS", "NonFarmPayrolls_SA_LogMoM_RT",
                                  obs_start="1939-01-01").reindex(idx)

    # 03 — Nominal PCE (PCE): ALFRED first-release MoM%
    print("  Fetching PCE (first-release RT) ...")
    cols["PCE_SA_LogMoM_RT"] = build_rt_mom("PCE", "PCE_SA_LogMoM_RT",
                               obs_start="1967-01-01").reindex(idx)

    # 04 — Real PI ex transfers (W875RX1): current vintage, log-diff MoM
    #print("  Fetching W875RX1 (current vintage) ...")
    #w875 = to_me(fred_fetch("W875RX1", obs_start="1959-01-01"))
    #cols["RealPrivInc_ExTransf_SA_LogMoM_LastVint"] = np.log(w875.replace(0, np.nan)).diff(1).reindex(idx)

    # 04 — Real PI ex transfers (W875RX1): current vintage, log-diff MoM
    print("  Fetching W875RX1 (current vintage) ...")
    w875 = to_me(fred_fetch("W875RX1", obs_start="1959-01-01"))
    # ── Microsoft special dividend correction ─────────────────────────────────
    # On December 2, 2004, Microsoft paid a $32B special dividend. BEA recorded
    # ~$24.85B as personal dividend income, boosting December 2004 personal
    # income by $298.2B at annual rate in NOMINAL terms — a pure accounting
    # artifact with no business-cycle content.
    # Source: https://www.bea.gov/index.php/help/faq/77
    #
    # W875RX1 is in Billions of Chained 2017 Dollars, so the nominal $298.2B
    # must be deflated to 2017 dollars using the PCE price index (PCEPI),
    # which is the deflator BEA uses for personal income series.
    # correction_2017$ = 298.2 × (PCEPI_2017_avg / PCEPI_2004_Dec)
    # We subtract this amount from the December 2004 level only — the BEA
    # recorded the full dividend in December as a one-time payment, so no
    # adjustment is needed in any other month.
    _dec04 = pd.Timestamp("2004-12-31")
    if _dec04 in w875.index:
        print("  Fetching PCEPI for Microsoft dividend deflation ...")
        pcepi = to_me(fred_fetch("PCEPI", obs_start="2000-01-01"))
        _pcepi_dec04  = pcepi.get(_dec04, np.nan)
        _pcepi_2017   = pcepi.loc["2017-01-31":"2017-12-31"].mean()
        if not np.isnan(_pcepi_dec04) and not np.isnan(_pcepi_2017):
            _msft_nominal   = 298.2                          # BEA figure, nominal $B annualised
            _msft_real_2017 = _msft_nominal * (_pcepi_2017 / _pcepi_dec04)
            w875[_dec04]   -= _msft_real_2017
            print(f"    W875RX1 Microsoft dividend correction: "
                  f"nominal={_msft_nominal:.1f}B  "
                  f"PCEPI ratio={_pcepi_2017/_pcepi_dec04:.4f}  "
                  f"real_2017$={_msft_real_2017:.1f}B  "
                  f"subtracted from {_dec04.date()}")
        else:
            print("    ⚠ PCEPI unavailable — Microsoft dividend correction skipped")

    cols["RealPrivInc_ExTransf_SA_LogMoM_LastVint"] = (
        np.log(w875.replace(0, np.nan)).diff(1).reindex(idx))

    # 05 — Aggregate Weekly Hours (Philly Fed hMvMd): first-release MoM%
    print("  Loading Philly Fed h (aggregate weekly hours) ...")
    try:
        df_h = pd.read_excel(philly_h_file, index_col=0, header=0)
        df_h.index = df_h.index.astype(str).str.strip()
        df_h = df_h.apply(pd.to_numeric, errors="coerce")
        # MoM% within each vintage (row-wise pct_change)
        mom_h = df_h.pct_change(periods=1, axis=0) * 100
        # First-release: first non-NaN vintage per row
        # First-release annualized growth rate per period
        fr_ann = mom_h.apply(
            lambda row: row.dropna().iloc[0] if row.notna().any() else np.nan,
            axis=1)
        # Convert annualized % to log MoM: log((1 + ann/100)^(1/12))
        fr_h = np.log((1 + fr_ann / 100) ** (1/12))
        # Parse index: '2024:05' → month-end Timestamp
        idx_h = []
        for lbl in fr_h.index:
            try:
                yr, mo = str(lbl).strip().split(":")
                idx_h.append(pd.Timestamp(int(yr), int(mo), 1)
                             + pd.offsets.MonthEnd(0))
            except Exception:
                idx_h.append(pd.NaT)
        fr_h.index = pd.DatetimeIndex(idx_h)
        fr_h = fr_h[fr_h.index.notna()].sort_index()
        cols["AggWeeklyHours_SA_LogMoM_PhillyRT"] = fr_h.reindex(idx)
        last_philly = fr_h.last_valid_index()
        print(f"  {'hours':20s}: {fr_h.notna().sum()} Philly first-release obs  "
              f"last={last_philly.strftime('%Y-%m') if last_philly else 'n/a'}")
        if MODE == "latest" and last_philly and last_philly < pd.Timestamp.today() - pd.DateOffset(months=6):
            print(f"    ⚠ Philly Fed h file may be stale (last={last_philly.date()}). "
                  f"Re-download from: https://www.philadelphiafed.org/-/media/FRBP/Assets/"
                  f"Surveys-And-Data/real-time-data/data-files/xlsx/hMvMd.xlsx")
    except Exception as e:
        print(f"    ⚠ Philly Fed h file: {e}")
        cols["AggWeeklyHours_SA_LogMoM_PhillyRT"] = pd.Series(np.nan, index=idx)

    # 06 — Initial Claims (ICSA): monthly sum, 11m log-diff, negated
    print("  Fetching ICSA (current vintage, monthly sum, 11m log-diff neg) ...")
    icsa_w = fred_fetch("ICSA", obs_start="1967-01-01")
    icsa_m_sum = icsa_w.resample("ME").sum()
    log_sum    = np.log(icsa_m_sum.replace(0, np.nan))
    neg_11m    = -log_sum.diff(11)
    cols["InitialClaims_SA_Log11mNeg_LastVint"] = neg_11m.reindex(idx)

    df = pd.DataFrame(cols, index=idx)
    df.index.name = "date"
    print(f"\n  Monthly macro shape: {df.shape}")
    print(f"  NaN counts:\n{df.isna().sum().to_string()}")
    return df


