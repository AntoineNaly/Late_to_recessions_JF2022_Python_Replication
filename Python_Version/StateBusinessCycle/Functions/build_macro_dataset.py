def build_ted_replacement() -> pd.Series:
    """
    Builds a continuous TED-equivalent spread from 1971 to present by
    splicing two FRED commercial paper series and subtracting the T-bill:
      CP3M   : 3-Month Prime Commercial Paper (Apr 1971 – Aug 1997, monthly)
      DCPF3M : 90-Day AA Financial CP         (Jan 1997 – present,  daily)
      TB3MS  : 3-Month T-Bill rate            (monthly)
    A mean-shift correction is applied over the 7-month overlap (Jan-Aug 1997).
    Only used in MODE == "latest" (TEDRATE was discontinued Jan 2022).
    """
    cp_old  = fred_fetch("CP3M").resample("ME").last()
    cp_new  = fred_fetch("DCPF3M").resample("ME").last()
    tbill   = fred_fetch("TB3MS").resample("ME").last()

    # Mean-shift correction over overlap period
    overlap_old = cp_old.loc["1997-01-01":"1997-08-31"]
    overlap_new = cp_new.loc["1997-01-01":"1997-08-31"]
    aligned     = pd.concat([overlap_old, overlap_new], axis=1).dropna()
    level_gap   = (aligned.iloc[:, 0] - aligned.iloc[:, 1]).mean()
    cp_old_adj  = cp_old - level_gap

    # Splice: old series up to Dec 1996, new series from Jan 1997
    cp_spliced = pd.concat([
        cp_old_adj[cp_old_adj.index < "1997-01-01"],
        cp_new[cp_new.index >= "1997-01-01"]
    ]).sort_index()

    # Spread = CP - T-bill
    combined = pd.concat([cp_spliced, tbill], axis=1).dropna()
    spread   = (combined.iloc[:, 0] - combined.iloc[:, 1]).rename("TED_replacement")
    print(f"  TED replacement: {spread.notna().sum()} obs  "
          f"{spread.first_valid_index().date()} → {spread.last_valid_index().date()}  "
          f"(gap={level_gap:+.4f}%)")
    return spread

def get_monthly_financial() -> pd.DataFrame:
    """
    Returns DataFrame with columns:
      sp500, t10y2y, tedrate, baa_aaa, t10yie, vix
    All z-scored ready (levels or YoY%).
    """
    print("\n── Monthly financial series ─────────────────────────────────")
    idx = make_monthly_index(end=END_DATE)
    cols = {}

    def fetch_daily_to_monthly(series_id: str) -> pd.Series:
        """Daily → monthly AVERAGE (confirmed by correlation analysis vs author)."""
        s = fred_fetch(series_id)   # no obs_start → fetch from inception
        if len(s) == 0:
            return pd.Series(dtype=float)
        s.index = pd.to_datetime(s.index)
        return s.resample("ME").mean()

    # 07 — S&P 500: month-end close, YoY% log return
    print("  Downloading ^GSPC (Yahoo Finance, month-end close, YoY log) ...")
    try:
        gspc_d = (yf.Ticker("^GSPC")
                    .history(start="1948-01-01", end=END_DATE,
                             auto_adjust=True)["Close"])
        gspc_d.index = pd.DatetimeIndex(gspc_d.index).tz_localize(None)
        gspc_d = gspc_d.sort_index().dropna()
        gspc_m = gspc_d.resample("ME").last()
        sp500_yoy = np.log(gspc_m).diff(12)
        cols["SP500_LogYoY"] = sp500_yoy.reindex(idx)
        print(f"  {'sp500':20s}: {gspc_m.notna().sum()} monthly obs")
    except Exception as e:
        print(f"    ⚠ ^GSPC: {e}")
        cols["SP500_LogYoY"] = pd.Series(np.nan, index=idx)

    # 08 — T10Y2Y level (10Y-2Y spread, positive = normal curve)
    print("  Fetching T10Y2Y ...")
    t10y2y = fetch_daily_to_monthly("T10Y2Y")
    cols["T10Y2Y"] = t10y2y.reindex(idx)
    print(f"    T10Y2Y: {t10y2y.notna().sum()} obs  {t10y2y.first_valid_index()}")

    # 09 — TEDRATE negated (or CP-Tbill replacement in latest mode)
    if MODE == "replicate":
        print("  Fetching TEDRATE ...")
        ted = fetch_daily_to_monthly("TEDRATE")
        cols["TED_neg"] = (-ted).reindex(idx)
        print(f"    TEDRATE: {ted.notna().sum()} obs  {ted.first_valid_index()}")
    else:
        print("  Building CP-Tbill spread (TEDRATE replacement for latest mode) ...")
        ted = build_ted_replacement()
        cols["CPTbill_neg"] = (-ted).reindex(idx)

    # 10 — BAA-AAA spread negated (lower credit spread = better conditions)
    print("  Fetching BAA, AAA ...")
    baa = fetch_daily_to_monthly("BAA")
    aaa = fetch_daily_to_monthly("AAA")
    idx_common = baa.index.intersection(aaa.index)
    baa_aaa = (baa[idx_common] - aaa[idx_common])
    cols["BAA_AAA_neg"] = (-baa_aaa).reindex(idx)
    print(f"    BAA-AAA: {baa_aaa.notna().sum()} obs")

    # 11 — T10YIE level (positive): higher breakeven = higher inflation expectations
    print("  Fetching T10YIE ...")
    t10yie = fetch_daily_to_monthly("T10YIE")
    cols["Breakeven10Y"] = t10yie.reindex(idx)   # positive level: author confirmed +1.000 with T10YIE
    print(f"    T10YIE: {t10yie.notna().sum()} obs  {t10yie.first_valid_index()}")

    # 12 — VIX negated (lower VIX = lower fear = better)
    print("  Fetching VIXCLS ...")
    vix = fetch_daily_to_monthly("VIXCLS")
    cols["VIX_neg"] = (-vix).reindex(idx)
    print(f"    VIXCLS: {vix.notna().sum()} obs  {vix.first_valid_index()}")

    df = pd.DataFrame(cols, index=idx)
    df.index.name = "date"
    print(f"\n  Monthly financial shape: {df.shape}")
    print(f"  NaN counts:\n{df.isna().sum().to_string()}")
    return df


