def get_quarterly_macro() -> pd.DataFrame:
    """
    Returns DataFrame on MONTHLY index (NaN at non-quarter-end rows):
      gdp, res_inv, nres_inv
    All are QoQ log-diff first-release.
    """
    print("\n── Quarterly macro series ───────────────────────────────────")
    idx = make_monthly_index(end=END_DATE)
    cols = {}

    # 13 — Nominal GDP: QoQ log-diff first-release (confirmed corr=0.99 vs GDPC1=0.51)
    print("  Fetching GDP nominal (first-release RT) ...")
    cols["NomGDP_SA_LogQoQ_RT"] = build_rt_qoq("GDP", "NomGDP_SA_LogQoQ_RT").reindex(idx)

    # 14 — Nominal Residential Fixed Inv (PRFI): first-release QoQ log-diff
    print("  Fetching PRFI (first-release RT) ...")
    cols["NomResFixedInv_SA_LogQoQ_RT"] = build_rt_qoq("PRFI", "NomResFixedInv_SA_LogQoQ_RT").reindex(idx)

    # 15 — Nominal Nonresidential Fixed Inv (PNFI): first-release QoQ log-diff
    print("  Fetching PNFI (first-release RT) ...")
    cols["NomNResFixedInv_SA_LogQoQ_RT"] = build_rt_qoq("PNFI", "NomNResFixedInv_SA_LogQoQ_RT").reindex(idx)

    df = pd.DataFrame(cols, index=idx)
    df.index.name = "date"

    # Verify quarterly alignment
    for col in df.columns:
        non_nan = df[col].dropna()
        qe = non_nan.index.month.isin([3, 6, 9, 12]).sum()
        print(f"  {col:20s}: {len(non_nan)} obs, {qe} at quarter-end months")
    return df


